def sumar():
    print("Suma")
    # pido un numero y almaceno a una variable
    numeroUno = input("Ingrese un numero: ")
    # parseo a un numero
    numeroUno = int(numeroUno)
    # pido un numero y almaceno a una variable
    numeroDos = input("Ingrese otro numero: ")
    # parseo a un numero
    numeroDos = int(numeroDos)
