# Dada una lista de n√∫meros, imprimir la suma de todos los elementos de la lista.

numeros = list(range(0, 11))
cantidad_de_numeros = len(numeros)
acumulador_numeros = 0
contador = 0
while(contador < cantidad_de_numeros):
    acumulador_numeros += numeros[contador]
    contador += 1
print("La suma de todos los numeros de la lista es: ", acumulador_numeros)















